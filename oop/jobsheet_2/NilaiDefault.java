public class NilaiDefault {
    static byte a;
    static short b;
    static int c;
    static long d;
    static float e;
    static double f;
    static boolean g;
    static char h;

    public static void main(String[] args) {
        System.out.println("Nilai default dari byte: " + a);
        System.out.println("Nilai default dari short: " + b);
        System.out.println("Nilai default dari int: " + c);
        System.out.println("Nilai default dari long: " + d);
        System.out.println("Nilai default dari float: " + e);
        System.out.println("Nilai default dari double: " + f);
        System.out.println("Nilai default dari boolean: " + g);
        System.out.println("Nilai default dari char: " + h);
    }
}