public class percobaan_8 {
  public static void main(String[] args) {
    int x = -1;
    int y = -1;
    System.out.println("Operator Bitwise - Complement");
    System.out.println("Nilai Awal");
    System.out.println("Nilai x = " + x);
    System.out.println("Nilai y = " + y);
    System.out.println("Operasi: x = ~x");
    System.out.println("Nilai x = " + ~x);
    System.out.println("Operasi: y = ~y");
    System.out.println("Nilai y = " + ~y);
    }
   
}
